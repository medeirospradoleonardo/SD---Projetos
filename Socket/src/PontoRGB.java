public class PontoRGB {
    private int valorR;
    private int valorG;
    private int valorB;

    public PontoRGB() {
    }

    public PontoRGB(int valorR, int valorG, int valorB) {
        this.valorR = valorR;
        this.valorG = valorG;
        this.valorB = valorB;
    }

    public int getValorR() {
        return this.valorR;
    }

    public void setValorR(int valorR) {
        this.valorR = valorR;
    }

    public int getValorG() {
        return this.valorG;
    }

    public void setValorG(int valorG) {
        this.valorG = valorG;
    }

    public int getValorB() {
        return this.valorB;
    }

    public void setValorB(int valorB) {
        this.valorB = valorB;
    }

}
